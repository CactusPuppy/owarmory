<h1>Thank You, What's Next, and Goodbye</h1>
<p>
  While we've been thrilled with the reception OW Armory has received, unfortunately both of the
  active developers of OW Armory have not found ourselves as passionate about Stadium as the rest of
  the community has been. While we could continue to develop and maintain OW Armory, we'd be
  delivering a half-baked product, which you, the community, do not deserve.
</p>
<p>
  However, you all have put a humbling amount of work into making the builds on this site, and I'd
  personally hate to see all that work go to waste. To that end, we've spoken with <a
    href="https://stadiumbuilds.io"
    target="_blank"
    rel="noopener noreferrer">Stadium Builds</a
  >, and they're willing to help us port all of your builds to their site!
</p>
<p>
  So what's next? <em
    >Effective immediately, new builds cannot be made, and existing builds cannot be edited.</em
  >
  This will ensure that we capture all the builds in our data export to Stadium Builds, and will
  also ensure that everyone knows that Stadium Builds is the place to go to share builds. After the
  build transfer is complete, <em>OW Armory will shut down</em> for good.
</p>
<p>
  Thanks for the kind words and patronage over the past few months, but it's time to close the
  armory and let you all get out into the fray.
</p>
<p>Humbly Yours,</p>
<p>- CactusPuppy and Mitsiee</p>

